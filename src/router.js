  async function getAmount(amountIn, reserveIn, reserveOut) {
    try {
       const valueInWe1 = ethers.utils.parseUnits(amountIn.toString(), contract.decimals);
      
      let valueInWei=ethers.BigNumber.from(valueInWe1.toString());
      
      console.log("Value in Wei:", valueInWei.toString());
      reserveIn=ethers.BigNumber.from(reserveIn.toString());
      reserveOut=ethers.BigNumber.from(reserveOut.toString());

      //const result = await contract.getAmountOut(valueInWei, reserveIn, reserveOut); // Replace with the name of your function
      //let a=parseInt(result, 10)

      let amountInWithFee = valueInWei.mul(997);
      let numerator = amountInWithFee.mul(reserveOut); //29,910
      let denominator = reserveIn.mul(1000).add(amountInWithFee); //22,955
      let amountOut = numerator / denominator; //
      amountOut/=10**18

      //let a=ethers.utils.formatEther(result)
      console.log('Result:', amountOut);
      return amountOut
    } catch (error) {
      console.error('Error:', error);
    }
  }
